
public class 반복문예제1 {

	public static void main(String[] args) {
		//1. for문을 활용해서 21에서 57까지 숫자 중 홀수만 출력하기
		//(출력 결과가 가로로 나오기)
		
		for(int num = 21;num<=57;num+=2) {
			System.out.print(num +" ");
		}
		
		System.out.println();
		//2. for문을 활용해서 96에서 53까지 출력하기
		// (출력 결과가 가로로 나오기)
				
		for(int i = 96; i>=53 ; i--) {
			System.out.print(i+" ");
		}
		
		
	}

}
